
package shapes;

public class TwoD  {

   private double Base;
   private double Altura;
   
   TwoD (double B, double A){
	   Base = B;
	   Altura = A;
   }
   
public double getBase() {
	return Base;
}
public void setBase(double base) {
	Base = base;
}
public double getAltura() {
	return Altura;
}
public void setAltura(double altura) {
	Altura = altura;
}
          
    
}


